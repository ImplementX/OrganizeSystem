package com.tjrac.organization.pojo;

public class OrganizationStudent {
    private Integer organizationStudentId;

    private Integer organizationId;

    private Integer studentId;

    public Integer getOrganizationStudentId() {
        return organizationStudentId;
    }

    public void setOrganizationStudentId(Integer organizationStudentId) {
        this.organizationStudentId = organizationStudentId;
    }

    public Integer getOrganizationId() {
        return organizationId;
    }

    public void setOrganizationId(Integer organizationId) {
        this.organizationId = organizationId;
    }

    public Integer getStudentId() {
        return studentId;
    }

    public void setStudentId(Integer studentId) {
        this.studentId = studentId;
    }
}